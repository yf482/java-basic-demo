public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        System.out.printf("i:" + i);
        System.out.printf("++i:" + ++i);
        System.out.printf("i++:" + i++);
        System.out.printf("i:" + i);
        System.out.printf("--i:" + --i);
        System.out.printf("i--:" + i--);
        System.out.printf("i:" + i);

    }
}
